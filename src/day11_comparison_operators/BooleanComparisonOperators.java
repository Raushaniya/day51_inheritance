package day11_comparison_operators;

public class BooleanComparisonOperators {
    public static void main(String[] args) {
        System.out.println("10==10"); //this is true
        System.out.println(1000>100); //true
        System.out.println(985.44 < 98544.77); //true
        System.out.println(10 <= 9); //false
        System.out.println(5 >= 3);// true
        System.out.println(-100 != 44); //true -100 is not equaĞ´ 44

        int a = 100;
        int b = 200;
        System.out.println(a == b); //falsew
        System.out.println(a > b); //false
        System.out.println(a < b); //true
        System.out.println(a >= b); //false
        System.out.println(a <= b); //true
        System.out.println(a != b); //true

        boolean result;
        result = 5 == 5;
        System.out.println("result = " + result); // result = true

        result = 33>44;
        System.out.println("result = " + result); // result = false






    }
}
